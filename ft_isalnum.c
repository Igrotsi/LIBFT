#include <stdio.h>

int	ft_isalnum(int i)
{
	return((i >= '0' && i <= '9') || (i >= 'Z' && i <= 'A') || (i >= 'a' && i <= 'z'));
}

int     main(void)
{
	printf("%d", ft_isalnum('#'));
	return (0);
}