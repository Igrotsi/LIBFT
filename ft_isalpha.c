#include <stdio.h>

int	ft_isalpha(int i)
{
	return ((i >= 'A' && i <= 'Z') || (i >= 'a' && i <= 'z'));
}

int	main(void)
{
	printf("%d", ft_isalpha('e'));
	return (0);
}